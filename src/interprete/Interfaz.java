    public String obtenerDataArchivo(String path)
    {
        String data = null;
        if(path==null){return null;}
        path = path.replace("\\\\","\\");
        try 
        {
            File archivo= new File(path); 
            if(!archivo.exists())
            {
                Utilidades.Singlenton.registrarErrorSemantico(path, "El archivo no ha sido encontrado", 0, 0);
                JOptionPane.showMessageDialog(this, "El archivo " + path + " no se ha encontrado.", "Error", WIDTH);                 
                return data;
            }
            Scanner sc = new Scanner(archivo);
            data = "";
            while (sc.hasNextLine())
            {
                if(data.equals(""))
                {
                    data = sc.nextLine();
                }
                else
                {
                    data = data + "\n" + sc.nextLine();
                }
            }            
        } 
        catch (HeadlessException | FileNotFoundException e) 
        {
            /*Si no existe el archivo, marcar error semantico.*/
                Utilidades.Singlenton.registrarErrorSemantico(path, "El archivo no ha sido encontrado", 0, 0);
                JOptionPane.showMessageDialog(this, "El archivo " + path + " no se ha encontrado.", "Error", WIDTH);                 
                return data;           
        }
        return data;
    }
    
    
