    public void analizarArchivoOlc(String pathArchivo)
    {
            try 
            { 
                Utilidades.Singlenton.pilaArchivos.add(pathArchivo);                
                scannerOlc lexico=new scannerOlc(new java.io.FileReader(pathArchivo));                                             
                parserOlc sintactico = new parserOlc(lexico);                              
                sintactico.parse();  
                
                Proyecto proyectoAbierto = sintactico.proyecto;
                if(proyectoAbierto != null)
                {
                    /*Aquí ya tenemos un proyecto*/
                    Utilidades.Singlenton.proyectos.add(proyectoAbierto);     
                    agregarProyectoAlArbol(proyectoAbierto);
                }
                
            } 
            catch (Exception ex) 
            {
                System.out.println(ex.getMessage());  
                mostrarErrores();                          
                if(Utilidades.Singlenton.listaErrores.isEmpty()){fechaHora+= "  No se han encontrado errores durante la ejecución.";}
                else{fechaHora+= "  Se han encontrado "+Utilidades.Singlenton.listaErrores.size()+" error(es) durante la ejecución.";}
                reporteCompilacion.setText(fechaHora);                  
            }            
            // Interpretamos
            try 
            {                
                if(sintactico.raiz!=null){generarGrafica(pathArchivo);}
                interpretar(sintactico.raiz);    
                /*Generar gráfica*/                
                mostrarErrores();                          
                if(Utilidades.Singlenton.listaErrores.isEmpty()){fechaHora+= "  No se han encontrado errores durante la ejecución.";}
                else{fechaHora+= "  Se han encontrado "+Utilidades.Singlenton.listaErrores.size()+" error(es) durante la ejecución.";}
                reporteCompilacion.setText(fechaHora);                  
            } catch (Exception e) 
                
            {
                mostrarErrores();                          
                if(Utilidades.Singlenton.listaErrores.isEmpty()){fechaHora+= "  No se han encontrado errores durante la ejecución.";}
                else{fechaHora+= "  Se han encontrado "+Utilidades.Singlenton.listaErrores.size()+" error(es) durante la ejecución.";}
                reporteCompilacion.setText(fechaHora);                  
            }        
    }
    
    
