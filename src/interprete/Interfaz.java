    public double seno(double x, int ajuste)
    {
        double contador=0;
        if( ajuste>= 1 && ajuste<=10)
        {
            Imprimir("El ajuste debe estar entre 1 y 10");
            return -1;
        }
        double valor=0;
        ajuste = ajuste*100;
        while(contador<=ajuste)
        {
            valor = valor + (Math.pow(-1,contador)/(factorial(2*contador+1)))*Math.pow(x, 2*contador);
        }
        return valor;
    }
    
    
